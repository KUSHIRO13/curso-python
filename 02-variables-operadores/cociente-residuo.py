#Practica 1
print("Vamos a sacar el cociente de dos numeros enteros")
num1 = input("Ingrese un numero: ")
num2 = input("Ingrese el segundo numero: ")

#Conversor
num1 = int(num1)
num2 = int(num2)

#Divicion
#Cociente
resultado = num1 // num2
print("El cociente es: ",resultado)
#Residuo
resultado= num1 % num2
print("Y el residuo es: ", resultado)
